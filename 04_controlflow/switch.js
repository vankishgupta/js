//  SWITCH CASE 
// BASIC SYNTAX FOR SWITCH CASE
// switch (key) {
//     case value:
        
//         break;

//     default:
//         break;
// }

//example 
const month=3;
switch (month) {
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("april");
        break;
    
    default:
        console.log("default case");
        break;
}